func main() {
	configPath := flag.String("config", "", "Path to the config file")
	flag.Parse()

	logrus.Printf("config file path: %v", *configPath)
	cfg := &types.Config{}
	err := utils.ReadConfig(cfg, *configPath)

	if err != nil {
		logrus.Fatalf("error reading config file: %v", err)
	}
	utils.Config = cfg

	db.MustInitDB(cfg.Database.Username, cfg.Database.Password, cfg.Database.Host, cfg.Database.Port, cfg.Database.Name)
	defer db.DB.Close()

	result := []struct {
		ValidatorIndex  uint64 `json:"validator_index" db:"validatorindex"`
		ValidatorName   string `json:"validator_name" db:"name"`
		LastAttestation uint64 `json:"last_attestation" db:"lastattestationslot"`
		LastGraffiti    string `json:"last_graffiti" db:"lastgraffiti"`
		Eth1Address     string `json:"eth1_address" db:"eth1address"`
	}{}
	err = db.DB.Select(&result, `
		with 
			orderedblocks as (
				select proposer, slot, graffiti from blocks order by proposer, slot desc
			)
		select 
			v.validatorindex,
			coalesce(v.name, '') as name,
			coalesce(v.lastattestationslot,0) as lastattestationslot,
			(select ENCODE(from_address::bytea, 'hex') from eth1_deposits where publickey = v.pubkey limit 1) as eth1address,
			coalesce((select ENCODE(graffiti , 'escape') from orderedblocks where proposer = v.validatorindex limit 1),'\x00') as lastgraffiti
		from validators v
		where validatorindex = any($1)
		order by eth1address`,
		pq.Array(validators))
	if err != nil {
		logrus.Fatalf("error doing query: %v", err)
	}
	jsonB, err := json.Marshal(&result)
	if err != nil {
		logrus.Fatalf("error marshaling json: %v", err)
	}

	vByEth1 := map[string]int{}
	vByGraffiti := map[string]int{}
	for _, v := range result {
		_, exists := vByEth1[v.Eth1Address]
		if !exists {
			vByEth1[v.Eth1Address] = 1
		} else {
			vByEth1[v.Eth1Address]++
		}
		_, exists = vByGraffiti[v.LastGraffiti]
		if !exists {
			vByGraffiti[v.LastGraffiti] = 1
		} else {
			vByGraffiti[v.LastGraffiti]++
		}
	}

	vByEth12 := make([][]interface{}, len(vByEth1))
	i := 0
	for k, v := range vByEth1 {
		vByEth12[i] = []interface{}{k, v}
		i++
	}
	sort.Slice(vByEth12, func(i, j int) bool {
		return vByEth12[i][1].(int) > vByEth12[j][1].(int)
	})

	vByGraffiti2 := make([][]interface{}, len(vByGraffiti))
	i = 0
	for k, v := range vByGraffiti {
		vByGraffiti2[i] = []interface{}{k, v}
		i++
	}
	sort.Slice(vByGraffiti2, func(i, j int) bool {
		return vByGraffiti2[i][1].(int) > vByGraffiti2[j][1].(int)
	})

	vByEth1JSON, err := json.Marshal(&vByEth12)
	if err != nil {
		logrus.WithError(err).Fatal("cant marshal")
	}
	vByGraffitiJSON, err := json.Marshal(&vByGraffiti2)
	if err != nil {
		logrus.WithError(err).Fatal("cant marshal")
	}
	fmt.Printf(`{"validators":%s\n,"validators_by_eth1address:%s\n,"validators_by_graffiti":%s}n}`, jsonB, vByEth1JSON, vByGraffitiJSON)
}

